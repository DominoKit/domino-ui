package org.dominokit.domino.ui.button;

/** A constants class to reference css classes used in Button components */
public class ButtonStyles {
  /** The main css class for a button */
  public static final String BUTTON = "dom-ui-btn";
  /** a css class to change the button display to block */
  public static final String BUTTON_BLOCK = "dom-ui-btn-block";
  /** a css class to make the button looks like a link */
  public static final String BUTTON_LINK = "dom-ui-btn-link";
  /** the main css class for buttons toolbar */
  public static final String BUTTON_TOOLBAR = "dom-ui-btn-toolbar";
  /** a css class to change the button to a circle button */
  public static final String BUTTON_CIRCLE = "dom-ui-circle";
  /** a css class to style the arrow in a dropdown button */
  public static final String CARET = "dom-ui-caret";
  /** the main css class for a dropdown button */
  public static final String BUTTON_DROPDOWN = "dom-ui-btn-dropdown";
  /** the css class for a toggled dropdown button */
  public static final String DROPDOWN_TOGGLE = "dom-ui-dropdown-toggle";
  /** a css class to linkify a button */
  public static final String LINK = "dom-ui-link";
  /** the mani css class for buttons group */
  public static final String BTN_GROUP = "dom-ui-btn-group";
  /** the css class for a vertical buttons group */
  public static final String BTN_GROUP_VERTICAL = "dom-ui-btn-group-vertical";
  /** a css class to justify a button */
  public static final String JUSTIFIED = "dom-ui-justified";
  /** the main css class for bordered buttons */
  public static final String BUTTON_BORDERED = "dom-ui-btn-bordered";
  /** a css class to make a button bordered */
  public static final String BTN_GROUP_BORDERED = "dom-ui-bordered";
}
